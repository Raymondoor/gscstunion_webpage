2021年度GSC学連高校訪問プロジェクトの一環として、佼成学園女子中学高等学校SG（スーパーグローバル）クラスの高校1年生を対象に、同校で「研究に活かせるインタビュー技法」についてのワークショップ（計3回）を開催しました。

ワークショップでは、情報収集の際に必要な意識や方法・情報整理の仕方について学びました。また、大学生が考案した「仮想町」にて様々な役職を持つ町人役にインタビューを行い、町の抱える問題に対して解決策を創造するアクティビティを実施しました。

第1回・2回ワークショップでは、高校生が事前に準備された町についての資料を深く分析し、誰にどのようなインタビューを行うことが最も効果的かを計画立てた後にインタビューを行いました。その後、インタビューで得た予想とは異なる見解や村の現状を踏まえ、情報整理の思考法を利用した解決に向けての話し合いを実施しました。
そして最終回である第3回では、各グループで考え抜いた仮想町に対しての解決策を発表しました。効果的なインタビューによって得られた多角的な意見を反映させつつ、問題に対して柔軟なアプローチがとられた解決策を共有しました。

高校生からは、「難しいけど楽しい！」や「90分があっという間だった！」といった感想が多くありました。レクチャーやアクティビティを通し、我々大学生側も高校生の皆さんの吸収の速さと柔軟な思考に驚かされながら、共に多くを学びました。

